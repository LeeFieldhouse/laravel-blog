    /**
     * Display the specified resource.
     *
     * @param  \App\Category  $category
     * @return \Illuminate\Http\Response
     */
    public function show(Category $category)
    {
        $postId = [];
        foreach($category->posts as $post){
            array_push($postId, $post->id);
        } //Collects all post ids of this category and puts it in $postId array
        $posts = Post::whereIn('id', $postId)->paginate(5); //Fetches all posts matching $postId and paginates
        return view('categories.show')->with(['posts' => $posts, 'category' => $category]);
    }

